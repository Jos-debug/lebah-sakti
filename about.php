              <p>Pencak Silat merupakan salah satu budaya asli bangsa Indonesia, dimana banyak pakar dan pendekar pencak silat meyakini bahwa masyarakat Melayu menciptakan dan menggunakan ilmu beladiri ini sejak masa prasejarah untuk menghadapi alam yang keras agar dapat survive (bertahan hidup). </p>
             <p>Di Provinsi Sumatera Selatan memiliki banyak Seni pencak silat yang tersebar di Kabupaten/Kota dan Desa. Salah satunya adalah KUNTAU. Kuntau ini berasal dari Desa Kayu Ara Kecamatan Sekayu Kabupaten Musi Banyuasin, diperkuat dengan SK yang dikeluarkan Pasirah Marga Mantri Melayu Kewedenaan Musi Ilir tertanggal 15 Maret 1952.  Mulai diajarkan sejak tahun 1954-1974 dilingkungan keluarga dan masyarakat desa Kayu Ara Sekayu. Pada tahun 1982 kuntau ini berkembang di Palembang. Kuntau ini diciptakan oleh Husin Ibal  hasil gabungan yang didapatkannya dari beberapa guru Kuntau diantaranya : 1.Zainuri, 2.Cemong, 3.Asla, 4.Ate, 5.Muin, 6.Tjik Usin, 7. Sastro. Pencak silat ini menganut aliran “Kuntau Melayu alias Kuntau Usin dari Marga Mantri Melayu”. Gerakan silat ini terdiri 12 tari dan diperkaya juga dengan Seni Olah Pernafasan bagi murid yang dianggap telah siap fisik maupun bathin. </p>
             <p>Perguruan Pencak Silat Lebah Sakti bergabung di Ipsi kota Palembang pada tahun 1987 yang kemudian perlahan mulai melebarkan sayapnya masuk dan berkembang di Bangka Belitung pada tahun 2007 kemudian resmi menjadi anggota Ipsi Bangka pada tahun 2008, hingga pada tahun 2020 Perguruan Pencak Silat Lebah Sakti resmi menjadi anggota Ipsi provinsi Bangka Belitung, lalu mulai berkemang di daerah lainnya seperti Pengda provinsi Kepri, Pengda provinsi Jawa Barat, selanjutnya akan terus berkembang di daerah lainnya. Insyaallah siap mencetak generasi yang berkualitas dan beradab.</p>
